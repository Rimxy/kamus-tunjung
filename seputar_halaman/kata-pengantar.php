            <h3 style="font-family: 'Merriweather', serif; color: var(--secondary-color); margin-top: 2rem;">
                KATA PENGANTAR
            </h3>
            <p>Puji syukur kehadirat Tuhan Yang Maha Esa atas rahmat dan karunia-Nya, sehingga naskah laporan Penyusunan Kamus Digital Bahasa Tunjung Indonesia Inggris di Kabupaten Kutai Barat ini dapat diselesaikan dengan baik. Dokumen ini merupakan hasil nyata dari komitmen Pemerintah Kabupaten Kutai Barat, melalui BAPPEDA LITBANG, untuk melaksanakan mandat pelestarian kebudayaan lokal sekaligus mendorong inovasi berbasis teknologi. Bahasa Tunjung (Tonyoi), sebagai warisan luhur Suku Tunjung dan identitas fundamental masyarakat Kutai Barat, menghadapi tantangan modernisasi dan globalisasi. Oleh karena itu, upaya pendokumentasian dan revitalisasi ini menjadi krusial.</p>
            <p>Penyusunan kamus dalam format digital dan multibahasa (Tunjung Indonesia Inggris) memiliki signifikansi ganda:</p>

            <ul class="about-list">
                <li><strong>Aksesibilitas dan Pelestarian:</strong> Kamus digital akan memastikan Bahasa Tunjung dapat diakses dengan mudah oleh generasi muda Tunjung, peneliti, dan masyarakat luas, melampaui batas geografis.</li>
                <li><strong>Jembatan Komunikasi Global:</strong> Kehadiran padanan kata dalam Bahasa Inggris membuka pintu bagi pengenalan kebudayaan Tunjung ke kancah internasional, serta mendukung sektor pariwisata dan penelitian lintas budaya.</li>
                <li><strong>Dukungan Pendidikan:</strong> Menjadi sumber referensi otentik yang sangat berharga bagi institusi pendidikan dan upaya pengajaran bahasa daerah.</li>
            </ul>

            <p>Kami menyampaikan apresiasi setinggi-tingginya kepada Tim Ahli Pusat Riset Preservasi, Bahasa dan Sastra Badan Riset dan Inovasi Nasional, para tokoh adat, budayawan, dan seluruh pihak yang telah berkontribusi aktif dalam pengumpulan data, verifikasi, dan penyusunan kamus ini. Kerja keras kolektif ini adalah wujud sinergi antara pemerintah, masyarakat adat, dan dunia akademik.</p>
            <p>Kami berharap Kamus Digital Bahasa Tunjung ini tidak hanya menjadi produk penelitian semata, namun menjadi instrumen yang hidup dan berkelanjutan. Kami mendorong pemanfaatan kamus ini secara maksimal oleh seluruh elemen masyarakat, serta berharap agar upaya digitalisasi dan inovasi berbasis kearifan lokal dapat terus dikembangkan di masa mendatang.</p>
            <p>Semoga Kamus Digital ini dapat memberikan manfaat besar bagi penguatan identitas budaya dan pembangunan berkelanjutan di Kabupaten Kutai Barat.</p>

