            <h3 class="content-subtitle">Format Entri dalam Kamus</h3>
            <p>Setiap entri umumnya memuat:</p>
            <ul class="about-list">
                <li>Lema (kata yang menjadi pokok entri),</li>
                <li>Kelas kata (kategori gramatikal),</li>
                <li>Makna dalam bahasa Indonesia dan bahasa Inggris,</li>
                <li>Contoh kalimat dalam bahasa Tunjung beserta terjemahannya,</li>
                <li>Catatan budaya (jika perlu).</li>
            </ul>

            <h4 style="margin-top: 1.5rem; color: var(--primary-color);">Contoh Format:</h4>
            <div class="example-block">kaat v pergi (Indonesia)          go (Inggris)
                Contoh: bapak kaat noang umaq
                ‘bapak pergi menebang pohon di ladang’
                ‘(my) father went to cut down a tree in the field’</div>

            <h3 class="content-subtitle">Penyajian Kamus</h3>
            <p>
                Lema disusun menurut abjad, baik secara horizontal maupun secara vertikal. Secara horizontal, misalnya, lema yg mempunyai deretan huruf baling diletakkan sesudah lema yang memiliki deretan huruf balik; sedangkan secara vertikal, huruf b diletak kan di bawah huruf a, huruf c diletakkan di bawah huruf b, dan seterusnya. Urutan lema disusun sebagai berikut.
            </p>
            <ol type="a" style="margin-left: 1.5rem; margin-bottom: 1rem;">
                <li>Lema</li>
                <li>Gabungan kata dari bentuk dasar</li>
            </ol>

            <p>Kata entri ditulis dengan huruf kecil seperti (kecuali yang tergolong nama diri)</p>
            <div class="example-block">daai         a      ‘habis’         ‘finish’
                hagatn         num ‘enam’         ‘six’</div>

