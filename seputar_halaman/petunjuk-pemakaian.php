            <h2 class="about-title">PETUNJUK PEMAKAIAN</h2>

            <h3 class="content-subtitle">Tujuan Penyusunan Kamus</h3>
            <p>Kamus ini disusun untuk:</p>
            <ul class="about-list">
                <li>Membantu penutur asli Tunjung dalam menjaga, menggunakan, dan mengembangkan bahasa;</li>
                <li>Menjadi bahan ajar di sekolah-sekolah lokal dan pusat belajar komunitas;</li>
                <li>Mendukung peneliti, pendokumentasi bahasa, dan mahasiswa yang mempelajari bahasa dan budaya Dayak Tunjung;</li>
                <li>Memperkuat identitas budaya, pengetahuan tradisional, dan sistem nilai masyarakat Tunjung.</li>
            </ul>

            <h3 class="content-subtitle">Ejaan</h3>
            <p style="text-indent: 2em;">
                Pada umumnya abjad bahasa Tunjung sama dengan abjad bahasa Indonesia yang terdiri atas fonem vokal dan fonem konsonan. Secara fonetis, bahasa Tunjung memiliki ciri khas sebagai bahasa yang mengenal adanya bunyi hentian glotal (ʔ). Selain itu, adanya konsonan rangkap khas, seperti ng /ŋ/, ny /ɲ/, sy /ʃ/, dan q sebagai penanda hamzah. Vokal /e/ pepet atau /ə/ dan /é/ lebih terbuka sering dibedakan dalam ucapan. Dalam vokal terdapat kata-kata yang tersusun atas fonem vokal tunggal, deretan vokal yang homogen (seperti aa yang umumnya diucapkan panjang /a:/) dan fonem vokal rangkap, seperti / ai, au, oi/.
            </p>

            <h3 class="content-subtitle">Sistem Urutan Abjad</h3>
            <p>
