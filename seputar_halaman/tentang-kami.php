            </p>
            <p>Tujuan utama pengembangan laman ini adalah untuk:</p>
            <ul class="about-list">
                <li>Memberikan akses informasi leksikal yang mudah dan terbuka bagi masyarakat umum, pelajar, dan peneliti;</li>
                <li>Menjadi sarana dokumentasi digital yang menghimpun kosakata, contoh penggunaan, dan konteks budaya penuturnya; dan</li>
                <li>Mendorong partisipasi masyarakat lokal dalam pelestarian dan pengembangan bahasa daerah.</li>
            </ul>
            <p>
