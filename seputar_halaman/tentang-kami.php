            <h2 class="about-title" style="margin-top: 2rem;">ABOUT US 🇬🇧</h2>
            <p>
                <strong>The Tunjung Dictionary</strong> is the official online platform for searching words and translations in the Dayak Tunjung language, a regional language spoken by the Dayak Tunjung community in Kutai Barat Regency, East Kalimantan, Indonesia.
            </p>
            <p>
                This website was developed as part of the Collaborative Program “Development of the Dayak Tunjung–Indonesian–English Digital Dictionary”, conducted between the Research Center for Language and Literature Preservation, Research Organization for Archaeology, Language, and Literature, National Research and Innovation Agency (BRIN), and the Regional Development Planning, Research, and Innovation Agency (Bappedalitbang) of Kutai Barat Regency, in close cooperation with the native speakers of the Dayak Tunjung language.
            </p>
            <p>The main objectives of this platform are to:</p>
            <ul class="about-list">
                <li>Provide open and accessible lexical information for the public, students, and researchers;</li>
                <li>Serve as a digital documentation medium that compiles vocabulary, examples of use, and the cultural context of its speakers; and</li>
                <li>Encourage community participation in the preservation and development of regional languages.</li>
            </ul>
            <p>
                Through this dictionary, users can explore the linguistic and cultural richness of the Dayak Tunjung people while contributing to the sustainability of Indonesia’s linguistic heritage.
            </p>
            <p class="tagline">
                ✨ Preserving language, nurturing culture.✨
